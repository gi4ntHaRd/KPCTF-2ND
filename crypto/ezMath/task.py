from secret import flag
from Crypto.Util.number import *

s = bytes_to_long(flag)
bits = s.bit_length()

while True:
    p = getPrime(bits + 1)
    if p > s:
        break
print(p)

x = getRandomRange(1, p)
y = getRandomRange(1, p)

for _ in range(3):
    s = (x * s + y) % p
    print(s)

